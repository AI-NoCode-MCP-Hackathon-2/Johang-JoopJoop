/**
 * 민감정보 마스킹 유틸리티
 * 계약서 텍스트에서 개인정보를 마스킹하여 보호합니다.
 */

export function maskSensitiveInfo(text: string): string {
  if (!text) return "";

  return text
    // 주민등록번호 (123456-1234567)
    .replace(/\d{6}-\d{7}/g, "******-*******")
    // 전화번호 (010-1234-5678, 02-123-4567)
    .replace(/\d{2,3}-\d{3,4}-\d{4}/g, "***-****-****")
    // 이메일
    .replace(/[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}/g, "email(마스킹)")
    // 날짜 (2024년 1월 1일)
    .replace(/\d{2,4}\s*년\s*\d{1,2}\s*월\s*\d{1,2}\s*일/g, "YYYY년 MM월 DD일")
    // 금액 (1,000원, 10000원)
    .replace(/[\d,]{3,}\s*원/g, "금액(마스킹)")
    // 6자리 이상 숫자 (계좌번호 등)
    .replace(/\d{6,}/g, "숫자(마스킹)");
}
